# Maak een programma dat een de gebruiker vraagt  om drie  kommagetallen in te geven en achterliggend voor je een bewerking uit met deze getallen. 
# Vervolgens zal je de uitkomst moeten printen in een duidelijke zin op het scherm.
kommagetal1  = float(input("Geef het eerste kommagetal: "))
kommagetal2 = float(input("Geef het tweede kommagetal: "))
kommagetal3 = float(input("Geef het derde kommagetal: "))


print("De som van de drie getallen is:", kommagetal1 + kommagetal2 + kommagetal3)
print("Het verschil van de drie getallen is:", kommagetal1 - kommagetal2 - kommagetal3)
print("Het product van de drie getallen is:", kommagetal1 * kommagetal2 * kommagetal3)
print("Het quotient van de drie getallen is:", kommagetal1 / kommagetal2 / kommagetal3)
